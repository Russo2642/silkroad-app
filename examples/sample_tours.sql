Озеро вытянуто вдоль долины, его длина составляет около 1,5 километров, а ширина варьируется от 200 до 400 метров. Максимальная глубина достигает порядка 20 метров.

Вода в озере кристально чистая, с характерным ярким бирюзово-синим оттенком, благодаря сочетанию чистого талого снега и минеральных примесей из горных пород. Температура воды летом редко превышает +12–14 °C. Озеро питается главным образом от таяния ледников и снегов, а также от горных ручьёв и небольших рек, стекающих с окружающих гор.

Окружающий ландшафт:
Озеро окружено густыми хвойными лесами, в основном елью и сосной, которые создают живописный зелёный пояс вокруг водоёма. Вдали возвышаются горные хребты Северного Тянь-Шаня, с каменистыми вершинами и ледниками. Вдоль берегов растут разнообразные кустарники, полевые цветы и травы, которые в теплое время года привлекают множество птиц и животных.

Флора и фауна:
В прибрежной зоне можно встретить редкие виды растений, характерные для горного пояса. В воде обитает форель, которую иногда можно увидеть, а также другие пресноводные рыбы. Вокруг озера живут косули, кабаны, зайцы, множество птиц — орлы, соколы, дятлы, которые создают живую экосистему. В утренние часы часто слышно пение птиц и шелест ветра в кронах деревьев.

Климат:
Климат здесь — умеренно-континентальный, с прохладным летом и холодной зимой. Летом температура воздуха днем достигает +20…+25 °C, но ночью может опускаться до +5 °C. Озеро замерзает на зиму, лед обычно держится с ноября по апрель.

Система включает три озера:
1. Нижнее Кольсай (1-е озеро) - Высота над уровнем моря: 1818 м. Доезжается на авто, самая посещаемая часть парка. Идеальное для пикника, рыбалки (форель), лодочных прогулок.

2. Среднее Кольсай (2-е озеро) - Высота: 2250 м. Пеший треккинг 7–9 км (~3–5 часов в одну сторону). Озеро окружено хвойным лесом и скалами, вода кристально чистая, отражает небо и ели как зеркало.

3. Верхнее Кольсай (3-е озеро) - Высота: 2850 м. Только для опытных туристов, поход 1–2 дня с палаткой. Находится у самой границы с Кыргызстаном, въезд туда строго регулируется.

Озеро Каинды:
Название: В переводе с казахского — "Берёзовое". Высота: 2000 м. Уникальность: Озеро образовалось в результате землетрясения в 1911 году, которое перегородило ущелье. Вода залила еловый лес, и до сих пор из воды торчат затопленные, серые, но стоящие деревья. Цвет воды: Невероятный бирюзовый оттенок, зависящий от погоды. Трекинг: К озеру можно пройти пешком (11 км из пос. Саты) или доехать на внедорожнике.

Как добраться:
Из Алматы: Направление через Каскелен – Кеген – Саты (всего ~300 км). Дорога асфальтирована до пос. Саты. После — гравийка. Каинды: Из Саты — 15 км до начала тропы. Затем — на УАЗ/ГАЗ (~5 км). Тип транспорта: Рекомендуется внедорожник или аренда с водителем.

